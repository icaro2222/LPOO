package questao4;

public class Setor {
	
	private String nome;
	private int andar;
	
	public Setor() {
	}
	
	public Setor(String nome, int andar) {
		super();
		this.nome = nome;
		this.andar = andar;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getAndar() {
		return andar;
	}
	public void setAndar(int andar) {
		this.andar = andar;
	}
	
	
}
